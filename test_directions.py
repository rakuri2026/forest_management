# Test each direction
for direction in ['north', 'east', 'south', 'west']:
    if direction == 'north':
        angle_condition = '(azimuth_deg >= 315 OR azimuth_deg < 45)'
    elif direction == 'east':
        angle_condition = '(azimuth_deg >= 45 AND azimuth_deg < 135)'
    elif direction == 'south':
        angle_condition = '(azimuth_deg >= 135 AND azimuth_deg < 225)'
    else:  # west
        angle_condition = '(azimuth_deg >= 225 AND azimuth_deg < 315)'

    query = text(f"""
        WITH input_geom AS (
            SELECT ST_GeomFromText(:wkt, 4326) as geom
        ),
        nearby AS (
            SELECT
                COALESCE(f.name, f.name_en, f.highway) as feature_name,
                ST_Distance(ST_Transform(i.geom, 32645), ST_Transform(f.geom, 32645)) as distance_m,
                degrees(ST_Azimuth(ST_Centroid(i.geom), ST_ClosestPoint(f.geom, ST_Centroid(i.geom)))) as azimuth_deg
            FROM infrastructure.road f, input_geom i
            WHERE ST_DWithin(ST_Transform(i.geom, 32645), ST_Transform(f.geom, 32645), 100)
        )
        SELECT COUNT(*) as count, STRING_AGG(DISTINCT feature_name, ', ') as features
        FROM nearby
        WHERE feature_name IS NOT NULL
        AND feature_name != ''
        AND {angle_condition}
    """)

    result = db.execute(query, {'wkt': wkt}).first()
    print(f"{direction.upper():8s}: {result.count} features")
    if result.features:
        print(f"          {result.features}")
    print()

